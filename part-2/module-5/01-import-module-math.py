import math
from math import factorial, pi as PI

x = 1.4
y = 2.6

print(math.floor(x), math.floor(y))
print(math.floor(-x), math.floor(-y))
print(math.ceil(x), math.ceil(y))
print(math.ceil(-x), math.ceil(-y))
print(math.trunc(x), math.trunc(y))
print(math.trunc(-x), math.trunc(-y))

print(factorial(8))
print(PI)
